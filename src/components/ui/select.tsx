/**
 * Select Component
 *
 * A customizable select component with RTL support and consistent styling.
 *
 * @example
 * ```tsx
 * <BaseSelect
 *   options={[
 *     { value: '1', label: 'אפשרות 1' },
 *     { value: '2', label: 'אפשרות 2' },
 *   ]}
 *   onChange={(value) => console.log(value)}
 * />
 * ```
 */

export interface SelectOption {
  value: string;
  label: string;
  disabled?: boolean;
}

export interface BaseSelectProps
  extends Omit<React.SelectHTMLAttributes<HTMLSelectElement>, "onChange"> {
  /** אפשרויות הבחירה */
  options: SelectOption[];
  /** האם הבחירה במצב שגיאה */
  error?: boolean;
  /** האם הבחירה במצב הצלחה */
  success?: boolean;
  /** פונקציה שתופעל בעת שינוי הערך */
  onChange?: (value: string) => void;
  /** טקסט ברירת מחדל */
  placeholder?: string;
}

export const BaseSelect = React.forwardRef<HTMLSelectElement, BaseSelectProps>(
  (
    {
      options,
      error,
      success,
      onChange,
      placeholder,
      className,
      value,
      ...props
    },
    ref
  ) => {
    const handleChange = (e: React.ChangeEvent<HTMLSelectElement>) => {
      onChange?.(e.target.value);
    };

    return (
      <div className="relative">
        <select
          ref={ref}
          value={value}
          onChange={handleChange}
          className={clsx(
            "w-full appearance-none rounded-md border transition-colors",
            "focus:outline-none focus:ring-2",
            "py-2 pl-3 pr-8 text-base",
            {
              "border-border-medium focus:border-brand-primary focus:ring-brand-primary/20":
                !error && !success,
              "border-action-error focus:border-action-error focus:ring-action-error/20":
                error,
              "border-action-success focus:border-action-success focus:ring-action-success/20":
                success,
              "text-text-tertiary": !value,
            },
            className
          )}
          {...props}
        >
          {placeholder && (
            <option value="" disabled>
              {placeholder}
            </option>
          )}
          {options.map((option) => (
            <option
              key={option.value}
              value={option.value}
              disabled={option.disabled}
            >
              {option.label}
            </option>
          ))}
        </select>
        <div className="pointer-events-none absolute inset-y-0 left-0 flex items-center px-2">
          <svg
            className="text-text-tertiary h-4 w-4"
            xmlns="http://www.w3.org/2000/svg"
            viewBox="0 0 20 20"
            fill="currentColor"
          >
            <path
              fillRule="evenodd"
              d="M5.293 7.293a1 1 0 011.414 0L10 10.586l3.293-3.293a1 1 0 111.414 1.414l-4 4a1 1 0 01-1.414 0l-4-4a1 1 0 010-1.414z"
              clipRule="evenodd"
            />
          </svg>
        </div>
      </div>
    );
  }
);

BaseSelect.displayName = "BaseSelect";

