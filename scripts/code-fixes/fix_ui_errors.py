def fix_ui_component(file_path):
    """מתקן רכיב UI ספציפי"""
    file_name = os.path.basename(file_path)
    component_name = file_name.replace('.tsx', '').replace('.jsx', '')
    
    if component_name == "button":
        content = get_button_template()
    elif component_name == "card":
        content = get_card_template()
    elif component_name == "dialog":
        content = get_dialog_template()
    elif component_name == "dropdown-menu":
        content = get_dropdown_menu_template()
    elif component_name == "toast":
        content = get_toast_template()
    else:
        content = get_ui_template(component_name)
    
    # הוספת האות הראשונה לאות גדולה לצורך שמירת השם בפסקל קייס
    return write_file(file_path, content.strip())

def fix_import_order(content):
    """מתקן סדר ייבוא בסיסי בקובץ קיים"""
    lines = content.split('\n')
    import_lines = []
    non_import_lines = []
    
    # מפריד את כל שורות הייבוא לקבוצה נפרדת
    for line in lines:
        if line.strip().startswith('import ') or line.strip().startswith('export type ') or line.strip().startswith('export interface '):
            import_lines.append(line)
        else:
            non_import_lines.append(line)
    
    # מסדר את הייבואים לפי קבוצות
    react_imports = [line for line in import_lines if 'react' in line.lower() or 'next' in line.lower()]
    external_imports = [line for line in import_lines if not (
        'react' in line.lower() or 
        'next' in line.lower() or 
        '@/' in line or 
        './' in line
    )]
    internal_imports = [line for line in import_lines if '@/' in line]
    relative_imports = [line for line in import_lines if './' in line]
    type_imports = [line for line in import_lines if 'export type ' in line or 'export interface ' in line]
    
    # מאחד את הקבוצות בסדר הנכון עם שורה ריקה ביניהן
    sorted_imports = []
    
    if react_imports:
        sorted_imports.extend(react_imports)
        sorted_imports.append('')
    
    if external_imports:
        sorted_imports.extend(external_imports)
        sorted_imports.append('')
    
    if internal_imports:
        sorted_imports.extend(internal_imports)
        sorted_imports.append('')
    
    if relative_imports:
        sorted_imports.extend(relative_imports)
        sorted_imports.append('')
    
    if type_imports:
        sorted_imports.extend(type_imports)
        sorted_imports.append('')
    
    # אם יש שורה ריקה אחרונה, מוריד אותה
    if sorted_imports and sorted_imports[-1] == '':
        sorted_imports.pop()
    
    # מאחד חזרה את הקובץ עם הייבואים המסודרים
    if sorted_imports and non_import_lines and non_import_lines[0].strip() != '':
        sorted_imports.append('')  # שורה ריקה בין הייבואים לקוד
    
    return '\n'.join(sorted_imports + non_import_lines)

def fix_file(file_path):
    """מתקן שגיאות בקובץ יחיד"""
    # טיפול מיוחד לקבצי UI
    if "/components/ui/" in file_path:
        if fix_ui_component(file_path):
            return True
    
    # טיפול רגיל - קריאת הקובץ, תיקון וכתיבה מחדש
    content = read_file(file_path)
    if not content:
        return False
    
    original_content = content
    content = fix_import_order(content)
    
    # מסיר סמיקולנים מיותרים
    content = re.sub(r';\s*;', ';', content)
    content = re.sub(r';\s*\n\s*;', ';\n', content)
    
    # רק אם היה שינוי, כותב את הקובץ מחדש
    if content != original_content:
        if write_file(file_path, content):
            return True
    
    return False

def process_files():
    """מעבד קבצים עם שגיאות UI ושגיאות סדר ייבוא"""
    # קבצי UI בעייתיים
    ui_files = [
        "src/components/ui/button.tsx",
        "src/components/ui/card.tsx",
        "src/components/ui/dialog.tsx",
        "src/components/ui/dropdown-menu.tsx",
        "src/components/ui/toast.tsx",
        # להוסיף רכיבים נוספים כנדרש
    ]
    
    # קבצים עם שגיאות סדר ייבוא
    import_files = glob.glob("src/app/api/**/*.ts") + glob.glob("src/app/api/**/*.tsx")
    
    # מאחד את כל הקבצים לטיפול
    all_files = ui_files + import_files
    
    fixed_count = 0
    total_count = len(all_files)
    
    for file_path in all_files:
        # מנרמל את הנתיב לפורמט אחיד
        normalized_path = file_path.replace('\\', '/')
        if not normalized_path.startswith('./'):
            normalized_path = './' + normalized_path
        
        if fix_file(normalized_path):
            fixed_count += 1
    
    print(f"\nנבדקו {total_count} קבצים, תוקנו {fixed_count} קבצים עם שגיאות UI וסדר ייבוא.")

if __name__ == "__main__":
    process_files() 