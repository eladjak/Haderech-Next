/** קטגוריות עם ספירת משאבים */
export const listCategories = query({
  args: {},
  handler: async (ctx) => {
    const all = await ctx.db.query("resources").collect();

    const categoryCounts = {
      guides: 0,
      exercises: 0,
      worksheets: 0,
      summaries: 0,
    } as Record<string, number>;

    for (const r of all) {
      categoryCounts[r.category] = (categoryCounts[r.category] ?? 0) + 1;
    }

    return [
      { id: "all", label: "כל המשאבים", count: all.length },
      { id: "guides", label: "מדריכים", count: categoryCounts.guides },
      { id: "exercises", label: "תרגילים", count: categoryCounts.exercises },
      { id: "worksheets", label: "דפי עבודה", count: categoryCounts.worksheets },
      { id: "summaries", label: "סיכומים", count: categoryCounts.summaries },
    ];
  },
});

