/** רשימת משאבים עם סינון לפי קטגוריה, סוג, וגישה */
export const listResources = query({
  args: {
    category: v.optional(categoryValidator),
    type: v.optional(typeValidator),
    isFree: v.optional(v.boolean()),
    search: v.optional(v.string()),
  },
  handler: async (ctx, args) => {
    let resources;

    if (args.category) {
      resources = await ctx.db
        .query("resources")
        .withIndex("by_category", (q) => q.eq("category", args.category!))
        .order("desc")
        .collect();
    } else {
      resources = await ctx.db.query("resources").order("desc").collect();
    }

    // Apply type filter
    if (args.type) {
      resources = resources.filter((r) => r.type === args.type);
    }

    // Apply free/premium filter
    if (args.isFree !== undefined) {
      resources = resources.filter((r) => r.isFree === args.isFree);
    }

    // Apply search filter
    if (args.search && args.search.trim()) {
      const q = args.search.trim().toLowerCase();
      resources = resources.filter(
        (r) =>
          r.title.toLowerCase().includes(q) ||
          r.description.toLowerCase().includes(q)
      );
    }

    return resources;
  },
});

