/** משאבים המועדפים של המשתמש */
export const getBookmarkedResources = query({
  args: {},
  handler: async (ctx) => {
    const identity = await ctx.auth.getUserIdentity();
    if (!identity) return [];

    const userId = identity.subject;

    const bookmarks = await ctx.db
      .query("resourceBookmarks")
      .withIndex("by_user", (q) => q.eq("userId", userId))
      .order("desc")
      .collect();

    const resources = await Promise.all(
      bookmarks.map((b) => ctx.db.get(b.resourceId))
    );

    return resources.filter(Boolean);
  },
});

/** בדיקה אם משאב מסומן כמועדף */
export const isResourceBookmarked = query({
  args: { resourceId: v.id("resources") },
  handler: async (ctx, args) => {
    const identity = await ctx.auth.getUserIdentity();
    if (!identity) return false;

    const existing = await ctx.db
      .query("resourceBookmarks")
      .withIndex("by_user_resource", (q) =>
        q.eq("userId", identity.subject).eq("resourceId", args.resourceId)
      )
      .first();

    return !!existing;
  },
});

// ─── Mutations ───────────────────────────────────────────────────────────────

/** מעקב הורדה + בדיקת גישה */
export const downloadResource = mutation({
  args: { resourceId: v.id("resources") },
  handler: async (ctx, args) => {
    const resource = await ctx.db.get(args.resourceId);
    if (!resource) throw new Error("Resource not found");

    // Premium check: if not free, require authentication
    if (!resource.isFree) {
      const identity = await ctx.auth.getUserIdentity();
      if (!identity) {
        return { success: false, reason: "auth_required" as const };
      }
    }

    // Increment download count
    await ctx.db.patch(args.resourceId, {
      downloadCount: resource.downloadCount + 1,
      updatedAt: Date.now(),
    });

    return { success: true, content: resource.content };
  },
});

/** הוספה / הסרה של סימנייה על משאב */
export const bookmarkResource = mutation({
  args: { resourceId: v.id("resources") },
  handler: async (ctx, args) => {
    const identity = await ctx.auth.getUserIdentity();
    if (!identity) throw new Error("Not authenticated");

    const userId = identity.subject;

    const existing = await ctx.db
      .query("resourceBookmarks")
      .withIndex("by_user_resource", (q) =>
        q.eq("userId", userId).eq("resourceId", args.resourceId)
      )
      .first();

    if (existing) {
      await ctx.db.delete(existing._id);
      return { action: "removed" as const };
    }

    await ctx.db.insert("resourceBookmarks", {
      userId,
      resourceId: args.resourceId,
      createdAt: Date.now(),
    });

    return { action: "added" as const };
  },
});

