export const seedWeeklyQuizzes = mutation({
  args: {},
  handler: async (ctx) => {
    const course = await ctx.db
      .query("courses")
      .filter((q) => q.eq(q.field("title"), COURSE_TITLE))
      .first();

    if (!course) {
      return {
        success: false,
        message: `Course "${COURSE_TITLE}" not found.`,
      };
    }

    const lessons = await ctx.db
      .query("lessons")
      .withIndex("by_course", (q) => q.eq("courseId", course._id))
      .collect();

    const byIndex = new Map(
      lessons.filter((l) => l.scriptIndex).map((l) => [l.scriptIndex as string, l])
    );

    let created = 0;
    let skipped = 0;
    const missing: string[] = [];

    for (const quiz of WEEKLY_QUIZZES) {
      const lesson = byIndex.get(quiz.lessonScriptIndex);
      if (!lesson) {
        missing.push(quiz.lessonScriptIndex);
        continue;
      }

      const existing = await ctx.db
        .query("quizzes")
        .withIndex("by_lesson", (q) => q.eq("lessonId", lesson._id))
        .first();
      if (existing) {
        skipped++;
        continue;
      }

      const quizId = await ctx.db.insert("quizzes", {
        lessonId: lesson._id,
        courseId: course._id,
        title: quiz.title,
        passingScore: quiz.passingScore,
        createdAt: Date.now(),
      });

      let order = 0;
      for (const q of quiz.questions) {
        await ctx.db.insert("quizQuestions", {
          quizId,
          question: q.question,
          options: q.options,
          correctIndex: q.correctIndex,
          explanation: q.explanation,
          order,
        });
        order++;
      }

      created++;
    }

    return { success: true, created, skipped, missingLessons: missing };
  },
});
