// Public mutation to run seed (for admin/setup)
export const runSeedDatingScenarios = mutation({
  args: {},
  handler: async (ctx) => {
    const existing = await ctx.db.query("dialogueScenarios").collect();
    if (existing.length > 0) {
      return {
        success: false,
        message: `Already have ${existing.length} dialogue scenarios. Skipping seed.`,
      };
    }

    const now = Date.now();
    const created: string[] = [];

    for (const scenario of DIALOGUE_SCENARIOS) {
      await ctx.db.insert("dialogueScenarios", {
        title: scenario.title,
        description: scenario.description,
        difficulty: scenario.difficulty,
        category: scenario.category,
        estimatedMinutes: scenario.estimatedMinutes,
        personaName: scenario.personaName,
        personaEmoji: scenario.personaEmoji,
        personaDescription: scenario.personaDescription,
        dialoguePoints: scenario.dialoguePoints.map((dp) => ({
          id: dp.id,
          situation: dp.situation,
          options: dp.options.map((opt) => ({
            text: opt.text,
            score: opt.score,
            feedback: opt.feedback,
          })),
          tip: dp.tip,
        })),
        published: scenario.published,
        order: scenario.order,
        createdAt: now,
      });
      created.push(scenario.title);
    }

    return {
      success: true,
      message: `Created ${created.length} dialogue scenarios.`,
      scenarios: created,
    };
  },
});
